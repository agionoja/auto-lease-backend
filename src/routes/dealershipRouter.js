import express from "express";
import * as dealershipController from "../controllers/dealershipController.js";
import { ROLES } from "../utils/constants.js";

const router = express.Router();

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// PUBLIC ROUTES
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
router.get("/:id", dealershipController.getDealership());
router.get("/", dealershipController.getDealerships);

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// DEALER ROUTES
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
router.route("/").post(dealershipController.createDealership());

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// DEALERSHIP ADMIN ROUTES
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
router
  .route("/admin/dealership")
  .get(dealershipController.getDealerships)
  .post(dealershipController.createDealership(ROLES.ADMIN));

router.route("/admin/dealership/:id");

export default router;
